for(let i = 1; i < data[id].seasons.length; i++) {
	season_tabs.push(season_tabs[0].cloneNode(true));
	season_tabs[i].id = "season_tab_" + (i+1);
	season_tabs[i].children[0].innerHTML = "Season " + (i+1);
	season_tabs[i-1].after(season_tabs[i]);
}

for(let i = 0; i < data[id].seasons.length; i++) {
	season_tabs[i].onclick = function() {
		setSeason(i);
	}
	if(i != selectedSeason) {
		season_tabs[i].style.backgroundImage = "linear-gradient(195deg, rgba(165, 30, 255, .3), rgba(165, 30, 255, .1))";
		season_tabs[i].children[0].style.opacity = .5;
	}
}

setSeason(0);

function setSeason(x) {
	selectedSeason = x;
	console.log(selectedSeason);

	for(let i = 0; i < data[id].seasons.length; i++) {
		if(i != selectedSeason) {
			season_tabs[i].style.backgroundImage = "linear-gradient(195deg, rgba(165, 30, 255, .3), rgba(165, 30, 255, .1))";
			season_tabs[i].children[0].style.opacity = .5;
		}
		else {
			season_tabs[i].style.backgroundImage = "linear-gradient(195deg, rgba(165, 30, 255, .7), rgba(165, 30, 255, .4))";
			season_tabs[i].children[0].style.opacity = 1;
		}
	}

	resetEpisodes();
}
//#endregion

//#region Episodes
function resetEpisodes() {
	for(let i = episodes.length - 1; i > 0; i--) {
		let removed = episodes.pop();
		removed.remove();
	}
	for(let i = 1; i < data[id].seasons[selectedSeason].episodes.length; i++) {
		episodes.push(episodes[0].cloneNode(true));
		episodes[i].id = "episode_" + (i+1);
		episodes[i-1].after(episodes[i]);
	}
	setEpisodes();
}
function setEpisodes() {
	for(let i = 0; i < data[id].seasons[selectedSeason].episodes.length; i++) {
		episodes[i].children[0].children[0].src = "./Episodes/" + data[id].title + "_S" + (selectedSeason+1) + "E" + (i+1) + ".png";
		episodes[i].children[1].children[0].innerHTML = data[id].seasons[selectedSeason].episodes[i].title;
		episodes[i].children[1].children[1].innerHTML = "Ep " + (i+1) + "&nbsp;&nbsp;-&nbsp;&nbsp;" + data[id].seasons[selectedSeason].episodes[i].duration;
		episodes[i].children[1].children[2].innerHTML = data[id].seasons[selectedSeason].episodes[i].synopsis;
	}
}
//#endregion

